// Объединённые ячейки в docx, которые нужно развернуть в несколько остановок
const SPLIT_STOPS: Record<string, string[]> = {
  'ул. М. Цветаевой, Маяк': ['Марины Цветаевой (Торта)', 'Маяк'],
}

export function mapStopName(docxName: string): string {
  const trimmed = docxName.trim()
  return STOP_MAP[trimmed] ?? trimmed
}

export function mapStopNames(docxName: string): string[] {
  const trimmed = docxName.trim()
  if (SPLIT_STOPS[trimmed]) return SPLIT_STOPS[trimmed]
  return [mapStopName(trimmed)]
}

// Остановки которые не включаются в расписание (нет в приложении)
const EXCLUDED_STOPS = new Set<string>([
  'Поликлиника (М. Цветаевой)',
])

export function isExcludedStop(docxName: string): boolean {
  return EXCLUDED_STOPS.has(docxName.trim())
}
